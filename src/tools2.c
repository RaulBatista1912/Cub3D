#include "../cub3d.h"

void	remove_white_space_at_the_end(char *line)
{
	int	i;

	i = ft_strlen(line);
	while (i > 0 && (line[i - 1] == ' ' || line[i - 1] == '\t'
			|| line[i - 1] == '\n'))
	{
		line[i - 1] = '\0';
		i--;
	}
}
